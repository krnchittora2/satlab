<br><p align="justify">The IIT Bombay Student Satellite Project is a landmark project taken up by IIT Bombay students. The objective of this project is to make IIT Bombay a respected centre for advancement in Satellite and Space Technology in the world. 'Pratham' is the first satellite under this project. The plan is to build a fully functional microsatellite which would then be launched by Indian Space Research Organization (ISRO). This is entirely a student initiative with mentorship provided by ISRO scientists and IIT Bombay Faculty. </p>
<br>
<p class="lead" style="text-align:justify;"><strong>'Pratham' has a Four-fold Mission Statement:</strong></p>
<ul style="text-align:left; padding:10px;" >
<li>Enabling students and faculty to gain knowledge and experience in the field of Satellite and Space technology.</li>

<li>Empowering the Satellite Team with the skills to develop the Satellite through various phases of Design, Analysis, Fabrication and Testing until the Flight Model is made.</li>

<li>Launching the satellite into orbit and measuring Total Electron Count of the Ionosphere.</li>

<li>Involving students from other universities in our Satellite mission by building ground stations in their universities.</li>
</ul><br>


