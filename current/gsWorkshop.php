<p align="justify">Payload of the satellite is to measure total electron content (TEC) of the ionosphere and to create tomographic map of TEC over India. More ground stations in India will facilitate to get more number of sample points and hence better resolution of map. Hence team Pratham as a part of social goal is aiming to involve other engineering colleges in the project by conducting ground station workshops and make them well equipped to setup a ground station in their campuses. The technical knowledge will be provided by Pratham team. The station setup can also be used to track and collect data from other satellites transmitting in the same frequency band as Pratham. </p>
<p align="justify">The TEC is useful for scientific studies, correcting errors in communication like GPS and also for Tsunami warnings. Currently, around 8 colleges are involved in this initiative. Atharva College of Engineering (ACE), Mumbai has setup an automated ground station which is being used for weather forecasting and satellite tracking. The ACE team published papers in renowned conferences and have been undertaking new projects like ‘Balloon-Sat’ used to monitor weather. Hence involvement in this project is not restricted to collection of TEC data for Pratham but can facilitate research and engineering in various related fields since the frequency band of Pratham is quite common to many other satellites. </p><br>
   

