<h2 style="text-align:left;"> On-Board Computer Sub-System</h2>


<p align="justify">The On-Board Computer Sub-system is like the brain of the satellite. It controls the way the satellite functions and is expected to take all the decisions for the satellite.</p>

<p align="justify">The on-board computer regulates the flow of data,executes the attitude-control loop and other such computational /decision tasks and also decides which sub-systems are to be kept on/off/idle in emergency situations. Since a failure of the OBC results in a failure of the satellite, a lot of emphasis is placed on robustness and the resistance of the OBC to errors (hardware/software) that may occur. Space-grade components are preferred to ensure a long life for the satellite.</p>

<p align="justify">The On-Board Computer team performs the following jobs :-</p>
<p align="justify">1. Collects data from the attitude sensors (magnetometers) and executes control loops (PID,H2 etc) and gives a feedback to the actuators (magnetorquers).</p>
<p align="justify">2. Collects house-keeping data like operational status of subsystems, power consumed, battery power left etc and based on this takes appropriate decisions.In case of emergency, this decision is sent to the Power Subsystem to take corrective action.</p>
<p align="justify">3. Initiates the transmission of data to the ground station at an appropriate time (i.e. when the satellite is visible from the ground station).</p>
<p align="justify">4. Collects data from other miscellaneous peripherals like the GPS, magnetometer.</p>


