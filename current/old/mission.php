<h3>Mission Statement of Pratham</h3>

<p align="justify"><b>'Pratham' has a Four-fold Mission Statement:</b> 
<br /><br />
<span style="font-style: italic">Enabling students and faculty to gain knowledge and experience in the field of Satellite and Space technology.</span></p>
<p align="justify" style="font-style: italic"> Empowering the Satellite Team with the skills to develop the Satellite through various phases of Design, Analysis, Fabrication and Testing until the Flight Model is made.</p>
<p align="justify" style="font-style: italic">Launching the satellite into orbit and measuring Total Electron Count of the Ionosphere.</p>
<p align="justify"><span style="font-style: italic"> Involving students from other universities in our Satellite mission by building ground stations in their universities.</span><br /> 
  <br />
  For the relevance of our satellite to the student community, we will be transmitting satellite data when the satellite passes over India so that any  interested university with a small ground station will not only be able to detect the beacon signal from our satellite but will also be able to measure TEC above their ground station. This is proposed to spread awareness among the  student community about this exciting field.</p>
<p align="justify">The  Pratham Project presents a wonderful learning experience  to the students of working on a real life multi-disciplinary complex  system.&nbsp;Learning enhancement through <a href="http://www.cdio.org/">CDIO</a> (Conceiving &ndash; Designing &ndash;  Implementing &ndash; Operating) has been one of the major objectives of this project. Since a lot of importance is attached to learning, it is duly visible in the Success Criterion for the Mission. </p>
