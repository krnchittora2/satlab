<h3>Communication Subsystem</h3>


  <p align="justify"><img src="images/comm.jpg" alt="IITBombay" width="336" height="262" class="float_right" />Communication system of the satellite is one the most important sub system, without which the satellite is just a rock revolving around the earth. Our main aim  is to make a working communication system which can transmit 2.5 kbps of data which includes housekeeping data. And assist the Payload Team in receiving their TEC Data.</p>
  <p align="justify">Communication Sub-System will perform the following  tasks:</p>
  <p align="justify"><br />
    1. On board antenna and Beacon: We have to design a satellite antenna to send the data to earth. After analysing our requirements  we  have decided to opt for  a  monopole antenna which can serve our purpose. A   &ambda;/4 monopole is what we will be using for data transmission in addition to a  beacon .<br />
    2. Modulation and demodulation: We have adopted frequency shift keying (FSK) as our modulation technique. We will have a FSK transmitter (modulator) on the satellite along with a power amplifier. The beacon, however, will be using CW.<br />
    3. Ground Station: To receive signal from the satellite, we will set up a ground station which will consist of satellite tracker, antennae, Low noise amplifier and a FSK demodulator and radio equipment to catch the Beacon signal. We will be using a pair of  crossed yagi antennae for the purpose of receiving the Mission Data  .The Crossed yagis are chosen as it is essential for the payload subsyatem for analysis of polarization. Low noise amplifier amplifies the signal it receives without adding much to its noise.</p>
