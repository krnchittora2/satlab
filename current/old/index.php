  <p align="justify"><img src="images/IITBSATGRP6.JPG" alt="IITBombay" width="329" height="230" class="float_right" />The IIT Bombay Student Satellite Project is a landmark project taken up by IIT Bombay students. The objective of this  project is to make IIT Bombay a respected centre for advancement in Satellite and Space Technology in the world. The project aims at launching at least 5 satellites within the next few years. These Satellites could be test-beds for new technology that is being developed in the institute and also a method for  space qualification. <a href="images/IITBSATGRP.JPG"> Click here to view enlarged image</a><br />
    <br />
    'Pratham' is the first satellite under this project. The plan is to build a fully functional microsatellite in less than three years which would then be launched by Indian Space Research Organisation (ISRO). This is entirely a student initiative with mentorship provided by ISRO scientists and IIT Bombay Faculty. The satellite will fit in a 30*30*30 cm cube and will weigh less than 15 kg.    <a href="mission.php">Click here</a> to view the Mission Statement of 'Pratham'. </p>
