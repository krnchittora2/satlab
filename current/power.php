<h2 style="text-align:left;">Power Sub-System</h2>

<p align="justify">The electrical power system of the satellite is like the heart of the satellite; it "pumps" electrical energy to the various parts of the satellite.</p>

<p align="justify">Being a very critical subsystem of the satellite, emphasis is put on simplicity and reliability. Space-grade solar cells are needed to ensure a long life of the satellite. Similarly, batteries which are qualified for space applications are preferred over untested batteries.</p>

<p align="justify">The Power Sub-System of the satellite does the following jobs:</p>
<p align="justify">1. It harnesses power from the Sun rays using high-efficiency solar cells.</p>
<p align="justify">2. The collected power is supplied to the various systems on board.</p>
<p align="justify">3. The remaining energy is stored in batteries. The satellite relies on this stored power when the Sun is not shining. The batteries are also used when the satellite consumes more power then the solar panels can provide.</p>
<p align="justify">4. The health of every system is monitored and faults (abnormally high power consumption) are reported to the main computer.</p>
<p align="justify">5. The power system has the capability to switch on and off any subsystem to conserve power.</p>

