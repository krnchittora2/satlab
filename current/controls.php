<h2 style="text-align:left;"> Controls Sub-System</h2>


<p align="justify">The Payload, Communications and Power Subsystems require the satellite to orient, rotate and stabilize itself. This is achieved by the Attitude Determination and Controls Subsystem.</p>

<p align="justify">The Attitude Determination And Control Subsystem performs the following tasks:</p>

<p align="justify">1. To stabilize the satellite once it is launched by the space-vehicle</p>
<p align="justify">2. To position it so that effective communication can be established with ground.</p>
<p align="justify">3. To orient it to optimise the power recieved by the satellite.</p>
