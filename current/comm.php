<h2 style="text-align:left;"> Communication Sub-System</h2>

<p align="justify">Communication system of the satellite is one the most important sub system, without which the satellite is just a rock revolving around the earth. Our main aim is to make a working communication system which can transmit 2.5 kbps of data which includes housekeeping data. And assist the Payload Team in receiving their TEC Data.</p>
<br>
<p align="justify">Communication Sub-System will perform the following tasks:</p>


<p align="justify">1. On board antenna and Beacon: We have to design a satellite antenna to send the data to earth. After analysing our requirements we have decided to opt for a monopole antenna which can serve our purpose. A &#955;/4 monopole is what we will be using for data transmission in addition to a beacon .</p>
<p align="justify">2. Modulation and demodulation: We have adopted frequency shift keying (FSK) as our modulation technique. We will have a FSK transmitter (modulator) on the satellite along with a power amplifier. The beacon, however, will be using CW.</p>
<p align="justify">3. Ground Station: To receive signal from the satellite, we will set up a ground station which will consist of satellite tracker, antennae, Low noise amplifier and a FSK demodulator and radio equipment to catch the Beacon signal. We will be using a pair of crossed yagi antennae for the purpose of receiving the Mission Data .The Crossed yagis are chosen as it is essential for the payload subsyatem for analysis of polarization. Low noise amplifier amplifies the signal it receives without adding much to its noise.</p>


<p align="justify">Payload: The payload represents the goal of the satellite. It stands for the purpose the satellite is going to serve, once it is in orbit.</p>

