<h2 style="text-align:left;"> Payload Sub-System</h2>
<p align="justify">Payload: The payload represents the goal of the satellite. It stands for the purpose the satellite is going to serve, once it is in orbit.</p>


<p align="justify">
With our first satellite 'Pratham', we wish to measure the ionospheric Total Electron Content (TEC) by the 'Faraday Beacon' method. Analysis of the total electron content gives us an insight into various characteristics of the ionosphere.
</p>
<p align="justify">
In order to measure the Total Electron Content, the satellite will have two parallel monopole antennae transmitting linearly polarized radio waves at frequencies of 400 and 433 MHz. The plane of polarization of these signals will rotate upon traveling through the ionosphere. By measuring the differential rotation of the plane of polarisation of these two signals, using crossed Yagi antennae, we can deduce the value of the Total Electron Content along the column through which the signals have passed. Analysis of the data from various ground stations (Ionosphere Tomography) will enable us to determine the distribution of electrons in different layers of the ionosphere.
</p>
<p align="justify">
TEC values are crucial for time delay and range error corrections for single frequency Global Positioning System (GPS) receivers. Latitudinal variation of TEC values over India will allow us to analyse the Equatorial Ionisation Anomaly (EIA) which has a bearing on the range of satellite based communication systems. The influence of solar activity on TEC values can also be studied.
</p>
<p align="justify">
We will require ground stations to be set up in various universities across India to capture the latitudinal variation in the values of TEC. This forms the social goal of our satellite. We wish to reach out to the students in various universities across India and invite them to become an integral part of our endeavour. This will give them an opportunity to learn the basics of satellite communication systems and satellite technology in general.
</p>
